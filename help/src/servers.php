<p align="left">This is where additional Asterisk servers. The number of servers in use is dependant on the licence purchase. This does not affect the number of servers you can add to the system.</p>

<p align="left">To add a server, click on the Add server button. The options are outlined in the table below.</p>
<table align="center">
	<tr>
		<th bgcolor="#000044"><font color="#ffffff"><b>Field</b></font></th>
		<th bgcolor="#000044"><font color="#ffffff"><b>Description</b></font></th>
	</tr>
	<tr>
		<td  bgcolor="#eeeeee">Asterisk Server Name</td>
		<td  bgcolor="#eeeeee">This is a name that you give the asterisk machine for reference in the dialer.</td>
	</tr>
	<tr>
		<td  bgcolor="#eeeeee">Asterisk Server Address</td>
		<td  bgcolor="#eeeeee">The hostname or IP Address of the Asterisk machine</td>
	</tr>
	<tr>
		<td  bgcolor="#eeeeee">Asterisk Server Username</td>
		<td  bgcolor="#eeeeee">The Asterisk manager username</td>
	</tr>
	<tr>
		<td  bgcolor="#eeeeee">Asterisk Server Password</td>
		<td  bgcolor="#eeeeee">The Asterisk manager password</td>
	</tr>
</table>

<p align="left">Once this has been completed you will need to make the server active. To do this, click on the play button <img src="images/control_play_blue.png"> and the status of the server will change to active (the red cross will change to a gree tick). To deactivate a server, click on the stop button <img src="images/control_stop_blue.png">, the green tick will change to a red cross.</p>

<p align="left">If you wish to edit the server information, click on the pencil icon <img src="images/pencil.png">.</p>
